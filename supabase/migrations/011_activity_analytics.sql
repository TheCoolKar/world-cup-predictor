-- Migration 011: idempotent terms acceptance + first-party activity analytics

-- ── Terms acceptance: one row per account/browser and terms version ─────────

alter table public.terms_acceptances
  add column if not exists anonymous_id uuid;

-- Keep the earliest legal acceptance when older clients produced duplicates.
delete from public.terms_acceptances t
using (
  select id
  from (
    select id,
           row_number() over (
             partition by user_id, version
             order by accepted_at asc, id asc
           ) as duplicate_number
    from public.terms_acceptances
    where user_id is not null
  ) ranked
  where duplicate_number > 1
) duplicates
where t.id = duplicates.id;

create unique index if not exists terms_acceptances_user_version_uidx
  on public.terms_acceptances (user_id, version)
  where user_id is not null;

create unique index if not exists terms_acceptances_browser_version_uidx
  on public.terms_acceptances (anonymous_id, version)
  where user_id is null and anonymous_id is not null;

-- ── Sessions ────────────────────────────────────────────────────────────────

create table if not exists public.app_sessions (
  id              uuid primary key,
  user_id         uuid references auth.users (id) on delete set null,
  anonymous_id    uuid not null,
  started_at      timestamptz not null default now(),
  last_seen_at    timestamptz not null default now(),
  active_seconds  integer not null default 0 check (active_seconds >= 0),
  page_views      integer not null default 0 check (page_views >= 0),
  entry_page      text,
  current_page    text,
  user_agent      text
);

create index if not exists app_sessions_started_at_idx
  on public.app_sessions (started_at desc);
create index if not exists app_sessions_last_seen_at_idx
  on public.app_sessions (last_seen_at desc);
create index if not exists app_sessions_user_id_idx
  on public.app_sessions (user_id) where user_id is not null;
create index if not exists app_sessions_anonymous_id_idx
  on public.app_sessions (anonymous_id);

alter table public.app_sessions enable row level security;

grant insert, update on public.app_sessions to anon, authenticated;
grant select on public.app_sessions to authenticated;

drop policy if exists "visitors can create their own sessions" on public.app_sessions;
create policy "visitors can create their own sessions"
  on public.app_sessions
  for insert
  to anon, authenticated
  with check (
    (auth.uid() is null and user_id is null) or
    (auth.uid() is not null and user_id = auth.uid())
  );

drop policy if exists "visitors can update their own sessions" on public.app_sessions;
create policy "visitors can update their own sessions"
  on public.app_sessions
  for update
  to anon, authenticated
  using (
    (auth.uid() is null and user_id is null) or
    (auth.uid() is not null and user_id = auth.uid())
  )
  with check (
    (auth.uid() is null and user_id is null) or
    (auth.uid() is not null and user_id = auth.uid())
  );

drop policy if exists "admins can read app sessions" on public.app_sessions;
create policy "admins can read app sessions"
  on public.app_sessions
  for select
  to authenticated
  using (
    exists (
      select 1 from public.profiles p
      where p.id = auth.uid() and p.is_admin
    )
  );

-- ── Activity events ─────────────────────────────────────────────────────────

create table if not exists public.app_activity_events (
  id            bigint generated by default as identity primary key,
  session_id    uuid not null references public.app_sessions (id) on delete cascade,
  user_id       uuid references auth.users (id) on delete set null,
  anonymous_id  uuid not null,
  event_name    text not null check (event_name ~ '^[a-z][a-z0-9_]{1,63}$'),
  page          text,
  metadata      jsonb not null default '{}'::jsonb check (jsonb_typeof(metadata) = 'object'),
  created_at    timestamptz not null default now()
);

create index if not exists app_activity_events_created_at_idx
  on public.app_activity_events (created_at desc);
create index if not exists app_activity_events_session_id_idx
  on public.app_activity_events (session_id);
create index if not exists app_activity_events_name_created_idx
  on public.app_activity_events (event_name, created_at desc);
create index if not exists app_activity_events_user_id_idx
  on public.app_activity_events (user_id) where user_id is not null;

alter table public.app_activity_events enable row level security;

grant insert on public.app_activity_events to anon, authenticated;
grant select on public.app_activity_events to authenticated;
grant usage, select on sequence public.app_activity_events_id_seq to anon, authenticated;

