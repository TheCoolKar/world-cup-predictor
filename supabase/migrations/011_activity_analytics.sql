-- ── Admin-only aggregate endpoint ───────────────────────────────────────────

create or replace function public.get_app_analytics(p_days integer default 30)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare
  safe_days integer := greatest(7, least(coalesce(p_days, 30), 90));
  result jsonb;
begin
  if not exists (
    select 1 from public.profiles p
    where p.id = auth.uid() and p.is_admin
  ) then
    raise exception 'Admin access required' using errcode = '42501';
  end if;

  with
  event_identities as (
    select
      e.created_at,
      e.session_id,
      e.user_id,
      e.anonymous_id,
      e.event_name,
      e.page,
      e.metadata,
      case
        when e.user_id is not null then 'user:' || e.user_id::text
        else 'anon:' || e.anonymous_id::text
      end as identity_key
    from public.app_activity_events e
  ),
  session_identities as (
    select
      s.*,
      case
        when s.user_id is not null then 'user:' || s.user_id::text
        else 'anon:' || s.anonymous_id::text
      end as identity_key
    from public.app_sessions s
  ),
  daily_series as (
    select generate_series(
      current_date - (safe_days - 1),
      current_date,
      interval '1 day'
    )::date as day
  ),
  daily_values as (
    select
      d.day,
      count(distinct e.identity_key) as active_users,
      count(distinct e.session_id) as sessions,
      coalesce(sum(
        case when e.event_name = 'heartbeat' and (e.metadata ->> 'active_seconds') ~ '^[0-9]+$'
          then greatest(0, least((e.metadata ->> 'active_seconds')::integer, 120))
          else 0
        end
      ), 0) as active_seconds
    from daily_series d
    left join event_identities e
      on e.created_at >= d.day::timestamptz
     and e.created_at < (d.day + 1)::timestamptz
    group by d.day
    order by d.day
  ),
  returning_users as (
    select identity_key, count(*) as session_count
    from session_identities
    where started_at >= now() - interval '30 days'
    group by identity_key
  ),
  popular_pages as (
    select page, count(*) as views
    from event_identities
    where event_name = 'page_view'
      and page is not null
      and created_at >= current_date - (safe_days - 1)
    group by page
    order by views desc, page
    limit 8
  ),
  popular_features as (
    select event_name, count(*) as uses
    from event_identities
    where event_name not in ('page_view', 'heartbeat', 'session_start')
      and created_at >= current_date - (safe_days - 1)
    group by event_name
    order by uses desc, event_name
    limit 8
  ),
  user_activity as (
    select
      e.user_id,
      p.username,
      p.avatar_url,
      u.email,
      count(distinct e.session_id) as sessions,
      count(*) filter (where e.event_name = 'page_view') as page_views,
      count(*) filter (
        where e.event_name not in ('page_view', 'heartbeat', 'session_start')
      ) as feature_actions,
      coalesce(sum(
        case when e.event_name = 'heartbeat' and (e.metadata ->> 'active_seconds') ~ '^[0-9]+$'
          then greatest(0, least((e.metadata ->> 'active_seconds')::integer, 120))
          else 0
        end
      ), 0) as active_seconds,
      max(e.created_at) as last_active_at
    from event_identities e
    left join public.profiles p on p.id = e.user_id
    left join auth.users u on u.id = e.user_id
    where e.user_id is not null
      and e.created_at >= current_date - (safe_days - 1)
    group by e.user_id, p.username, p.avatar_url, u.email
  ),
  ranked_user_activity as (
    select *
    from user_activity
    order by greatest(active_seconds, page_views * 30, sessions * 10) desc,
             last_active_at desc
    limit 100
  )
  select jsonb_build_object(
    'generated_at', now(),
    'range_days', safe_days,
    'dau', (
      select count(distinct identity_key) from event_identities
      where created_at >= current_date
    ),
    'wau', (
      select count(distinct identity_key) from event_identities
      where created_at >= current_date - 6
    ),
    'mau', (
      select count(distinct identity_key) from event_identities
      where created_at >= current_date - 29
    ),
    'sessions', (
      select count(*) from session_identities
      where started_at >= current_date - (safe_days - 1)
    ),
    'page_views', (
      select count(*) from event_identities
      where event_name = 'page_view'
        and created_at >= current_date - (safe_days - 1)
    ),
    'identified_users', (
      select count(distinct user_id) from event_identities
      where user_id is not null
        and created_at >= current_date - (safe_days - 1)
    ),
    'identified_page_views', (
      select count(*) from event_identities
      where event_name = 'page_view'
        and user_id is not null
        and created_at >= current_date - (safe_days - 1)
    ),
    'anonymous_page_views', (
      select count(*) from event_identities
      where event_name = 'page_view'
        and user_id is null
        and created_at >= current_date - (safe_days - 1)
    ),
    'identified_traffic_rate', (
      select case when count(*) = 0 then 0
        else round(100.0 * count(*) filter (where user_id is not null) / count(*), 1)
      end
      from event_identities
      where event_name = 'page_view'
        and created_at >= current_date - (safe_days - 1)
    ),
    'avg_active_seconds', (
      select coalesce(round(avg(active_seconds)), 0) from session_identities
      where started_at >= current_date - (safe_days - 1)
    ),
    'return_rate', (
      select case when count(*) = 0 then 0
        else round(100.0 * count(*) filter (where session_count > 1) / count(*), 1)
      end
      from returning_users
    ),
    'daily', (select coalesce(jsonb_agg(to_jsonb(daily_values) order by day), '[]'::jsonb) from daily_values),
    'popular_pages', (select coalesce(jsonb_agg(to_jsonb(popular_pages)), '[]'::jsonb) from popular_pages),
    'popular_features', (select coalesce(jsonb_agg(to_jsonb(popular_features)), '[]'::jsonb) from popular_features),
    'top_users', (
      select coalesce(
        jsonb_agg(to_jsonb(ranked_user_activity) order by active_seconds desc, page_views desc),
        '[]'::jsonb
      )
      from ranked_user_activity
    )
  ) into result;

  return result;
end;
$$;

revoke all on function public.get_app_analytics(integer) from public, anon;
grant execute on function public.get_app_analytics(integer) to authenticated;
